package java.sql;

public class SQLFeatureNotSupportedException extends SQLNonTransientException {
	public SQLFeatureNotSupportedException() {
		throw new RuntimeException("Stub!");
	}

	public SQLFeatureNotSupportedException(final String reason) {
		throw new RuntimeException("Stub!");
	}

	public SQLFeatureNotSupportedException(final String reason, final String SQLState) {
		throw new RuntimeException("Stub!");
	}

	public SQLFeatureNotSupportedException(final String reason, final String SQLState, final int vendorCode) {
		throw new RuntimeException("Stub!");
	}

	public SQLFeatureNotSupportedException(final Throwable cause) {
		throw new RuntimeException("Stub!");
	}

	public SQLFeatureNotSupportedException(final String reason, final Throwable cause) {
		throw new RuntimeException("Stub!");
	}

	public SQLFeatureNotSupportedException(final String reason, final String SQLState, final Throwable cause) {
		throw new RuntimeException("Stub!");
	}

	public SQLFeatureNotSupportedException(final String reason, final String SQLState, final int vendorCode,
			final Throwable cause) {
		throw new RuntimeException("Stub!");
	}
}
