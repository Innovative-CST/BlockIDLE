package java.sql;

public class SQLRecoverableException extends SQLException {
	public SQLRecoverableException() {
		throw new RuntimeException("Stub!");
	}

	public SQLRecoverableException(final String reason) {
		throw new RuntimeException("Stub!");
	}

	public SQLRecoverableException(final String reason, final String SQLState) {
		throw new RuntimeException("Stub!");
	}

	public SQLRecoverableException(final String reason, final String SQLState, final int vendorCode) {
		throw new RuntimeException("Stub!");
	}

	public SQLRecoverableException(final Throwable cause) {
		throw new RuntimeException("Stub!");
	}

	public SQLRecoverableException(final String reason, final Throwable cause) {
		throw new RuntimeException("Stub!");
	}

	public SQLRecoverableException(final String reason, final String SQLState, final Throwable cause) {
		throw new RuntimeException("Stub!");
	}

	public SQLRecoverableException(final String reason, final String SQLState, final int vendorCode,
			final Throwable cause) {
		throw new RuntimeException("Stub!");
	}
}
